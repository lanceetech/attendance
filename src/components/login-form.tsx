
'use client';

import { useRouter } from 'next/navigation';
import { Button } from '@/components/ui/button';
import { User, Shield, GraduationCap } from 'lucide-react';
import { useAuth, useFirestore } from '@/firebase';
import { signInWithCustomToken } from 'firebase/auth';
import { doc, setDoc } from 'firebase/firestore';
import { errorEmitter } from '@/firebase/error-emitter';
import { FirestorePermissionError } from '@/firebase/errors';
import { toast } from '@/hooks/use-toast';

const userProfiles = {
  admin: {
    uid: 'admin_user',
    name: 'Dr. Evelyn Reed',
    role: 'admin',
    email: 'e.reed@umma.ac.ke',
    avatar: 'admin_avatar',
  },
  lecturer: {
    uid: 'lecturer_user',
    name: 'Prof. Samuel Miles',
    role: 'lecturer',
    email: 's.miles@umma.ac.ke',
    avatar: 'lecturer_avatar',
  },
  student: {
    uid: 'student_user',
    name: 'Aisha Juma',
    role: 'student',
    email: 'a.juma@student.umma.ac.ke',
    avatar: 'student_avatar',
  },
};

export default function LoginForm() {
  const router = useRouter();
  const auth = useAuth();
  const firestore = useFirestore();

  const handleLogin = async (role: 'admin' | 'lecturer' | 'student') => {
    if (!auth || !firestore) {
      toast({
        variant: 'destructive',
        title: 'Firebase not initialized',
        description: 'Please try again in a moment.',
      });
      return;
    }

    try {
      const profileData = userProfiles[role];

      // This is a simplified token for the emulator.
      // In a real app, a proper JWT would be generated by a secure backend.
      const mockToken = `{"uid": "${profileData.uid}"}`;
      const userCredential = await signInWithCustomToken(auth, mockToken);
      const user = userCredential.user;

      if (user) {
        const userDocRef = doc(firestore, 'users', user.uid);

        // Set the user document, and chain a .catch for permission errors.
        setDoc(userDocRef, profileData, { merge: true })
          .then(() => {
            // On success, navigate to the dashboard.
            router.push('/dashboard');
          })
          .catch(error => {
            // On permission error, emit the contextual error.
            const permissionError = new FirestorePermissionError({
              path: userDocRef.path,
              operation: 'write',
              requestResourceData: profileData,
            });
            errorEmitter.emit('permission-error', permissionError);
          });
      }
    } catch (error) {
      console.error('Authentication failed:', error);
      toast({
        variant: 'destructive',
        title: 'Authentication Failed',
        description:
          'Could not sign in. Please check the console for more details.',
      });
    }
  };

  return (
    <div className="space-y-4">
      <Button
        onClick={() => handleLogin('admin')}
        className="w-full"
        variant="outline"
      >
        <Shield className="mr-2 h-4 w-4" />
        Sign in as Administrator
      </Button>
      <Button
        onClick={() => handleLogin('lecturer')}
        className="w-full"
        variant="outline"
      >
        <User className="mr-2 h-4 w-4" />
        Sign in as Lecturer
      </Button>
      <Button onClick={() => handleLogin('student')} className="w-full">
        <GraduationCap className="mr-2 h-4 w-4" />
        Sign in as Student
      </Button>
    </div>
  );
}
